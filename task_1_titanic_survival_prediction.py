#Import libraries
import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score,confusion_matrix, precision_score, recall_score, classification_report
import warnings
warnings.filterwarnings('ignore')

#Read and load data:
df=pd.read_csv('/content/drive/MyDrive/CodSoft_Internship/Titanic-Dataset.csv')

#Summary statistics
df.describe()

#Check missing values
df.isna().sum()

#Replace missing values of age with median of age:
df['Age'].fillna(df['Age'].median(), inplace=True)

#Replace missing values of embarked with mode of embarked:
df['Embarked'].fillna(df['Embarked'].mode()[0], inplace=True)

#Drop unnecessary Columns:
df.drop(columns=['Name', 'PassengerId'], inplace=True)

#Drop unnecessary Columns:
df.drop(columns=['Cabin', 'Ticket'], inplace=True)

#Convert object to integer:
df['Embarked'] = df['Embarked'].replace({'Q': 0, 'S': 1, 'C': 2})
df['Sex'] = df['Sex'].replace({'female': 1, 'male': 0})

#Print column names:
df.columns

#Model building
X=df.drop(['Survived'],axis=1)
y=df['Survived']
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.1, random_state=42)

lr = LogisticRegression() #Create model
lr.fit(X_train, y_train) #Fit the model
y_pred = lr.predict(X_test) #Predict the model

y_pred

accuracy = accuracy_score(y_test, y_pred)
print(f'Accuracy: {accuracy * 100:.2f}%')

cm = confusion_matrix(y_test, y_pred)
cm

precision = precision_score(y_test, y_pred, average='weighted')
precision

recall = recall_score(y_test, y_pred, average='weighted')
print(f'Accuracy: {recall * 100:.2f}%')

print(classification_report(y_test, y_pred))

